function reinicio_boton() {
  var myinput = document.getElementById("numerofalla");
  nombre = document.getElementById("nombrefalla");
  frec = document.getElementById("frecuencia");
  total = document.getElementById("minuto1");
  //RETURN DEFAULT VALUE
  myinput.value = myinput.defaultValue;
  nombre.value = nombre.defaultValue;
  frec.value = frec.defaultValue;
  total.value = total.defaultValue;
}

