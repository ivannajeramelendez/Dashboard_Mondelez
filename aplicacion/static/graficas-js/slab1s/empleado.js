function mostrar_empleado() {
  var numero = document.getElementById("empleado_numero"),
    nombre = document.getElementById("empleado_nombre");
  nombre.value = numero.value;
}

function mostrar_empleado_cambio() {
  var numero = document.getElementById("empleado_numero_cambio"),
    nombre = document.getElementById("empleado_nombre_cambio");
  nombre.value = numero.value;
}

